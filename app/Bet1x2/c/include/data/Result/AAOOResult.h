/// Arr[AOOResult *].

#ifndef DATA_RESULT_AAOORESULT_H
  #define DATA_RESULT_AAOORESULT_H

#include "dmc/Arr.h"
#include "data/Result/OAOOResult.h"

#include "data/Result/AOOResult.h"

/// Arr[AOOResult *].
struct aAOOResult_AAOOResult {
  AOOResult **es; // Start elements.
  AOOResult **end; // End elements. (Elements are between 'es' (inclusive) and 'end'
              // exclusive.
  AOOResult **endbf; // End buffer.
};

/// Arr[AOOResult *].
typedef struct aAOOResult_AAOOResult AAOOResult;

/// Creates a new Array with buffer size of 15 elements.
AAOOResult *aAOOResult_new (void);

/// 'buffer' must be > 0.
AAOOResult *aAOOResult_bf_new (int buffer);

/// Creates a new array from several elements.
/// Elements list must finish with NULL.
AAOOResult *aAOOResult_new_from (AOOResult *e, ...);

/// Creates a new array from a C array. For example:
///   Arr *a = arr_new_c(3, (void *[]){"c", "d", "e"});
/// If 'size' is less than C array length, result is ok (only will be
/// used 'size' first elements); but if 'size' is greater, the result is
/// undetermined.
AAOOResult *aAOOResult_new_c (int size, AOOResult **es);

/// Returns a new array with elements of 'this'.
AAOOResult *aAOOResult_copy (AAOOResult *this);

///
int aAOOResult_size (AAOOResult *this);

/// Resturn the element at position ix.
AOOResult *aAOOResult_get (AAOOResult *this, int ix);

/// Adds an element at the end of 'this'. 'e' will be freed by 'this'.
void aAOOResult_push (AAOOResult *this, AOOResult *e);

/// Returns and removes the last element.
AOOResult *aAOOResult_pop (AAOOResult *this);

/// Returns the las element.
AOOResult *aAOOResult_peek (AAOOResult *this);

/// Sets the element at position ix.
void aAOOResult_set (AAOOResult *this, int ix, AOOResult *e);

/// Inserts an element at position ix.
void aAOOResult_insert (AAOOResult *this, int ix, AOOResult *e);

/// Removes an element at position ix. Buffer size of 'this' does not change.
void aAOOResult_remove (AAOOResult *this, int ix);

/// Adds pointer to elements of 'other' to 'this'.
void aAOOResult_cat (AAOOResult *this, AAOOResult *other);

/// Inserts pointer to elements of 'other' at 'ix'
void aAOOResult_insert_arr (AAOOResult *this, int ix, AAOOResult *other);

/// Removes elements between [begin-end). Buffer size of 'this' does not change.
void aAOOResult_remove_range (AAOOResult *this, int begin, int end);

/// Removes every element of 'this'.
void aAOOResult_clear (AAOOResult *this);

/// Reverses elements of 'this'.
void aAOOResult_reverse (AAOOResult *this);

/// Sorts 'this' ascendantly using the function 'greater' that returns '1'
/// if 'e1' > 'e2'.
void aAOOResult_sort (AAOOResult *this, int (*greater)(AOOResult *e1, AOOResult *e2));

/// aAOOResult_shuflle remix 'this' elements. It should be used after calling
/// rnd_init() or sys_init().
void aAOOResult_shuffle (AAOOResult *this);

/// Returns '1' if every element of 'this' yields '1' with 'pred'.
int aAOOResult_all (AAOOResult *this, int (*pred)(AOOResult *e));

/// Returns '1' if some element of 'this' yields '1' with 'pred'.
int aAOOResult_any (AAOOResult *this, int (*pred)(AOOResult *e));

/// Returns the index of the first elements which returns '1'
/// with 'pred', or -1 if such element does not exist.
int aAOOResult_index (AAOOResult *this, int (*pred)(AOOResult *e));

/// Returns the index of the last elements which returns '1'
/// with 'pred', or -1 if such element does not exist.
int aAOOResult_last_index (AAOOResult *this, int (*pred)(AOOResult *e));

/// Returns the first element which pruduces '1' with 'pred' or 'tp_none'.
OAOOResult *aAOOResult_find(AAOOResult *this, int (*pred)(AOOResult *e));

/// Returns the last element which pruduces '1' with 'pred' or 'tp_none'.
OAOOResult *aAOOResult_find_last(AAOOResult *this, int (*pred)(AOOResult *e));

/// aAOOResult_filter_in removes every element which returns '0' with 'pred'.
void aAOOResult_filter_in (AAOOResult *this, int (*pred)(AOOResult *e));

/// Returns a new Arr with the n first elements of this.
/// If this has less elements than n, returs a copy of this.
AAOOResult *aAOOResult_take (AAOOResult *this, int n);

/// Returns a new Arr with the first elements which return '1' with 'predicate'.
AAOOResult *aAOOResult_takef (AAOOResult *this, int (*predicate)(AOOResult *e));

/// Returns a new Arr with elements left after aAOOResult_take.
AAOOResult *aAOOResult_drop (AAOOResult *this, int n);

/// Returns a new Arr with elements left after aAOOResult_takef.
AAOOResult *aAOOResult_dropf (AAOOResult *this, int (*predicate)(AOOResult *e));

/// Returns a new Arr with every element which returns '1' with 'pred'.
AAOOResult *aAOOResult_filter_to (AAOOResult *this, int (*predicate)(AOOResult *e));

/// Returns a new Arr with elements generated by converter.
Arr *aAOOResult_map (AAOOResult *this, void *(*converter)(AOOResult *e));

/// Returns a new Arr whit the first element generated by conv1 and the rest
/// by conv2.
Arr *aAOOResult_map2 (AAOOResult *this, void *(*conv1)(AOOResult *e), void *(*conv2)(AOOResult *e));

/// Returns a new Arr mixing values of 'a1' and 'a2'. The size of the resultant
/// array is the less of 'a1' size and 'a2' size.
Arr *aAOOResult_zip (AAOOResult *a1, AAOOResult *a2, void *(*converter)(AOOResult *e1, AOOResult *e2));

/// Returns a new Arr mixing values of 'a1', 'a2' and 'a3'. The size of the
/// resultant array is the less of 'a1' size, 'a2' size and 'a3' size.
Arr *aAOOResult_zip3 (
  AAOOResult *a1, AAOOResult *a2, AAOOResult *a3,
  void *(*conveter)(AOOResult*e1, AOOResult*e2, AOOResult*e3)
);

/// Removes duplicates with function 'feq=1' and returns them in a new array.
/// It returns only the first duplicated element.
AAOOResult *aAOOResult_duplicates (AAOOResult *this, int (feq)(AOOResult *e1, AOOResult *e2));

/// Returns this JSONized.
///   this: Container.
char *aAOOResult_to_js (AAOOResult *this);

/// Returns the container from its JSON representation.
///   js  : Container JSONized.
AAOOResult *aAOOResult_from_js (char *js);


//--// Not remove

#endif