/// Arr[AAOResult *].

#ifndef DATA_RESULT_AAAORESULT_H
  #define DATA_RESULT_AAAORESULT_H

#include "dmc/Arr.h"
#include "data/Result/OAAOResult.h"

#include "data/Result/AAOResult.h"

/// Arr[AAOResult *].
struct aAAOResult_AAAOResult {
  AAOResult **es; // Start elements.
  AAOResult **end; // End elements. (Elements are between 'es' (inclusive) and 'end'
              // exclusive.
  AAOResult **endbf; // End buffer.
};

/// Arr[AAOResult *].
typedef struct aAAOResult_AAAOResult AAAOResult;

/// Creates a new Array with buffer size of 15 elements.
AAAOResult *aAAOResult_new (void);

/// 'buffer' must be > 0.
AAAOResult *aAAOResult_bf_new (int buffer);

/// Creates a new array from several elements.
/// Elements list must finish with NULL.
AAAOResult *aAAOResult_new_from (AAOResult *e, ...);

/// Creates a new array from a C array. For example:
///   Arr *a = arr_new_c(3, (void *[]){"c", "d", "e"});
/// If 'size' is less than C array length, result is ok (only will be
/// used 'size' first elements); but if 'size' is greater, the result is
/// undetermined.
AAAOResult *aAAOResult_new_c (int size, AAOResult **es);

/// Returns a new array with elements of 'this'.
AAAOResult *aAAOResult_copy (AAAOResult *this);

///
int aAAOResult_size (AAAOResult *this);

/// Resturn the element at position ix.
AAOResult *aAAOResult_get (AAAOResult *this, int ix);

/// Adds an element at the end of 'this'. 'e' will be freed by 'this'.
void aAAOResult_push (AAAOResult *this, AAOResult *e);

/// Returns and removes the last element.
AAOResult *aAAOResult_pop (AAAOResult *this);

/// Returns the las element.
AAOResult *aAAOResult_peek (AAAOResult *this);

/// Sets the element at position ix.
void aAAOResult_set (AAAOResult *this, int ix, AAOResult *e);

/// Inserts an element at position ix.
void aAAOResult_insert (AAAOResult *this, int ix, AAOResult *e);

/// Removes an element at position ix. Buffer size of 'this' does not change.
void aAAOResult_remove (AAAOResult *this, int ix);

/// Adds pointer to elements of 'other' to 'this'.
void aAAOResult_cat (AAAOResult *this, AAAOResult *other);

/// Inserts pointer to elements of 'other' at 'ix'
void aAAOResult_insert_arr (AAAOResult *this, int ix, AAAOResult *other);

/// Removes elements between [begin-end). Buffer size of 'this' does not change.
void aAAOResult_remove_range (AAAOResult *this, int begin, int end);

/// Removes every element of 'this'.
void aAAOResult_clear (AAAOResult *this);

/// Reverses elements of 'this'.
void aAAOResult_reverse (AAAOResult *this);

/// Sorts 'this' ascendantly using the function 'greater' that returns '1'
/// if 'e1' > 'e2'.
void aAAOResult_sort (AAAOResult *this, int (*greater)(AAOResult *e1, AAOResult *e2));

/// aAAOResult_shuflle remix 'this' elements. It should be used after calling
/// rnd_init() or sys_init().
void aAAOResult_shuffle (AAAOResult *this);

/// Returns '1' if every element of 'this' yields '1' with 'pred'.
int aAAOResult_all (AAAOResult *this, int (*pred)(AAOResult *e));

/// Returns '1' if some element of 'this' yields '1' with 'pred'.
int aAAOResult_any (AAAOResult *this, int (*pred)(AAOResult *e));

/// Returns the index of the first elements which returns '1'
/// with 'pred', or -1 if such element does not exist.
int aAAOResult_index (AAAOResult *this, int (*pred)(AAOResult *e));

/// Returns the index of the last elements which returns '1'
/// with 'pred', or -1 if such element does not exist.
int aAAOResult_last_index (AAAOResult *this, int (*pred)(AAOResult *e));

/// Returns the first element which pruduces '1' with 'pred' or 'tp_none'.
OAAOResult *aAAOResult_find(AAAOResult *this, int (*pred)(AAOResult *e));

/// Returns the last element which pruduces '1' with 'pred' or 'tp_none'.
OAAOResult *aAAOResult_find_last(AAAOResult *this, int (*pred)(AAOResult *e));

/// aAAOResult_filter_in removes every element which returns '0' with 'pred'.
void aAAOResult_filter_in (AAAOResult *this, int (*pred)(AAOResult *e));

/// Returns a new Arr with the n first elements of this.
/// If this has less elements than n, returs a copy of this.
AAAOResult *aAAOResult_take (AAAOResult *this, int n);

/// Returns a new Arr with the first elements which return '1' with 'predicate'.
AAAOResult *aAAOResult_takef (AAAOResult *this, int (*predicate)(AAOResult *e));

/// Returns a new Arr with elements left after aAAOResult_take.
AAAOResult *aAAOResult_drop (AAAOResult *this, int n);

/// Returns a new Arr with elements left after aAAOResult_takef.
AAAOResult *aAAOResult_dropf (AAAOResult *this, int (*predicate)(AAOResult *e));

/// Returns a new Arr with every element which returns '1' with 'pred'.
AAAOResult *aAAOResult_filter_to (AAAOResult *this, int (*predicate)(AAOResult *e));

/// Returns a new Arr with elements generated by converter.
Arr *aAAOResult_map (AAAOResult *this, void *(*converter)(AAOResult *e));

/// Returns a new Arr whit the first element generated by conv1 and the rest
/// by conv2.
Arr *aAAOResult_map2 (AAAOResult *this, void *(*conv1)(AAOResult *e), void *(*conv2)(AAOResult *e));

/// Returns a new Arr mixing values of 'a1' and 'a2'. The size of the resultant
/// array is the less of 'a1' size and 'a2' size.
Arr *aAAOResult_zip (AAAOResult *a1, AAAOResult *a2, void *(*converter)(AAOResult *e1, AAOResult *e2));

/// Returns a new Arr mixing values of 'a1', 'a2' and 'a3'. The size of the
/// resultant array is the less of 'a1' size, 'a2' size and 'a3' size.
Arr *aAAOResult_zip3 (
  AAAOResult *a1, AAAOResult *a2, AAAOResult *a3,
  void *(*conveter)(AAOResult*e1, AAOResult*e2, AAOResult*e3)
);

/// Removes duplicates with function 'feq=1' and returns them in a new array.
/// It returns only the first duplicated element.
AAAOResult *aAAOResult_duplicates (AAAOResult *this, int (feq)(AAOResult *e1, AAOResult *e2));

/// Returns this JSONized.
///   this: Container.
char *aAAOResult_to_js (AAAOResult *this);

/// Returns the container from its JSON representation.
///   js  : Container JSONized.
AAAOResult *aAAOResult_from_js (char *js);


//--// Not remove

#endif